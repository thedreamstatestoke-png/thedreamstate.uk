 body:`<p class="drop">Launch day feels like a finish line. It's actually a starting gun. A new store is your best guess about how your customer behaves — and the first ninety days are when real traffic tells you which guesses were right and which were quietly wrong.</p>
<h2>Don't optimise everything. Optimise in order.</h2>
<p>The temptation post-launch is to fiddle with everything at once. Resist it. Work the funnel backwards from the money: find the biggest single drop-off, fix that, measure, then move to the next. One change at a time, so you actually learn what worked.</p>
<blockquote>A store launches as a hypothesis. The first 90 days are where you turn it into a machine — one fix at a time.</blockquote>
<h3>The 90-day order of operations</h3>
<ul>
<li>Weeks 1–2: watch, don't touch. Let real behaviour show you the leaks.</li>
<li>Weeks 3–6: fix the biggest drop-off — usually PDP or checkout.</li>
<li>Weeks 7–10: tighten mobile and the arrival experience.</li>
<li>Weeks 11–13: layer in proof, pay-later, and the first email flows.</li>
</ul>
<p>Done this way, the store gets sharper every week instead of busier. That disciplined first quarter is the difference between a launch that fades and one that compounds — and it's exactly the work a retainer is built to drive.</p>`
